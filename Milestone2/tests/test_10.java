public class SumOfArray {  
    public static void main(String[] args) {  
        //Initialize array  
        int arr[] = new int [5];  
        int sum = 0;  
        //Loop through the array to calculate sum of elements  
        for (int i = 0; i <33; i++) {  
           sum = sum + arr[i];  
        }  
    
    }  
}